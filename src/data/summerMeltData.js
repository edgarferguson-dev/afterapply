const joins = collegeCases.map((item) => {
  const student = students.find((entry) => entry.studentId === item.studentId);
  const staff = staffUsers.find((entry) => entry.staffId === item.assignedStaffId);
  const caseTasks = tasks
    .filter((entry) => entry.caseId === item.caseId)
    .sort((a, b) => new Date(a.dueDate) - new Date(b.dueDate));
  const caseMessages = messages
    .filter((entry) => entry.caseId === item.caseId)
    .sort(
      (a, b) =>
        new Date(a.sentAt || a.receivedAt || a.createdAt) -
        new Date(b.sentAt || b.receivedAt || b.createdAt),
    );
  const caseInterventions = interventions
    .filter((entry) => entry.caseId === item.caseId)
    .sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
  const caseCaseNotes = caseNotes.filter((entry) => entry.caseId === item.caseId);
  const completedCount = caseTasks.filter((entry) =>
    ["COMPLETED", "WAIVED"].includes(entry.taskStatus),
  ).length;
  const overdueCount = caseTasks.filter((entry) => entry.taskStatus === "OVERDUE").length;
  const blockedCount = caseTasks.filter((entry) =>
    ["BLOCKED", "OVERDUE"].includes(entry.taskStatus),
  ).length;
  const helpRequestedCount = caseTasks.filter((entry) => entry.helpRequested).length;
  const overdueTasks = caseTasks.filter((entry) => entry.taskStatus === "OVERDUE");
  const blockedTasks = caseTasks.filter((entry) => entry.taskStatus === "BLOCKED");
  const helpTasks = caseTasks.filter((entry) => entry.helpRequested);
  const escalated = caseInterventions.length > 0 || helpRequestedCount > 0;
  const urgentTask =
    caseTasks.find((entry) => ["OVERDUE", "BLOCKED", "IN_PROGRESS"].includes(entry.taskStatus)) ||
    caseTasks.find((entry) => !["COMPLETED", "WAIVED"].includes(entry.taskStatus)) ||
    caseTasks[0];

  const riskReasons = [
    overdueTasks.length > 0
      ? `${overdueTasks.length} overdue ${overdueTasks.length === 1 ? "task" : "tasks"}`
      : null,
    blockedTasks.length > 0
      ? `${blockedTasks.length} blocked ${blockedTasks.length === 1 ? "task" : "tasks"}`
      : null,
    helpTasks.length > 0
      ? `${helpTasks.length} help request${helpTasks.length === 1 ? "" : "s"} waiting`
      : null,
    !item.lastStudentResponseAt ? "student has not responded" : null,
    item.enrollmentStatus === "NOT_ATTENDING" ? "student changed plans" : null,
  ].filter(Boolean);

  const nextBestAction =
    overdueTasks.length > 0
      ? {
          title: "Resolve the overdue blocker",
          detail: `Contact the student about ${overdueTasks[0].taskName.toLowerCase()} and confirm the fastest path to completion.`,
          owner: staff.fullName,
        }
      : helpTasks.length > 0
        ? {
            title: "Respond to the help request",
            detail: `Student asked for support on ${helpTasks[0].taskName.toLowerCase()}. Send exact instructions or offer a call.`,
            owner: staff.fullName,
          }
        : !item.lastStudentResponseAt
          ? {
              title: "Try a manual outreach attempt",
              detail: "Student is silent. Send a status check and move to call outreach if there is still no response.",
              owner: staff.fullName,
            }
          : item.caseStatus === "CLOSED"
            ? {
                title: "Document the final outcome",
                detail: item.closeReason || "Case is closed. Confirm the outcome and archive follow-up notes.",
                owner: staff.fullName,
              }
            : {
                title: "Keep the case moving",
                detail: `Stay ahead of the next deadline for ${urgentTask.taskName.toLowerCase()} and keep reminders aligned to the due date.`,
                owner: staff.fullName,
              };

  const workflowState = {
    reminderSent: Boolean(urgentTask.lastReminderAt || item.lastOutboundMessageAt),
    overdue: urgentTask.taskStatus === "OVERDUE" || overdueTasks.length > 0,
    helpRequested: helpTasks.length > 0,
    escalated,
    completedOrClosed:
      item.caseStatus === "CLOSED" ||
      ["COMPLETED", "WAIVED"].includes(urgentTask.taskStatus),
  };

  return {
    ...item,
    student,
    staff,
    tasks: caseTasks,
    messages: caseMessages,
    interventions: caseInterventions,
    caseNotes: caseCaseNotes,
    progress: Math.round((completedCount / caseTasks.length) * 100),
    completedCount,
    overdueCount,
    blockedCount,
    helpRequestedCount,
    urgentTask,
    noResponse: !item.lastStudentResponseAt || overdueCount > 0,
    escalated,
    riskReasons,
    nextBestAction,
    workflowState,
  };
});

